#!/usr/bin/env node
// Generate items.csv rows for creature loot drops (v0.760, combat arc).
//
// creatures.csv loot tables reference ~130 item ids (raw_poultry, leather,
// dragon_scale...) that predate items.csv - a kill would drop nothing. Same
// closure as scripts/gen-harvest-items.js did for plants (v0.749): every
// referenced id gets a real `{id}_0` item row with keyword-derived category
// and physicals. Idempotent: existing ids (exact or _0) are skipped.
//
// Usage: node scripts/gen-loot-items.js

const fs = require('fs');
const path = require('path');

const ROOT = path.join(__dirname, '..');
const CREATURES = path.join(ROOT, 'data', 'creatures.csv');
const ITEMS = path.join(ROOT, 'data', 'items.csv');

// Column order: id,name,category,subcategory,base_material,weight_kg,
// stack_size,durability,description,content_class,volume_l

// Keyword rules, first match wins: [regex, category, subcategory,
// base_material, weight_kg, full description].
const RULES = [
  [/^raw_|_meat$|^alien_meat$|^rotten_flesh$/, 'food', 'protein', 'flesh', 0.6, 'Butchered creature meat'],
  [/^(fat|blubber|sky_blubber)$/, 'food', 'protein', 'flesh', 0.5, 'Rendered creature fat'],
  [/^(honey|beeswax)$/, 'food', 'sweetener', 'organic', 0.25, 'Sweet harvest from a creature'],
  [/hide$|^leather$|skin$|^fur$|^tattered_cloth$/, 'material', 'fiber', 'leather', 0.7, 'Hide or pelt from a creature'],
  [/scale$|^chitin_|shell/, 'material', 'creature_part', 'chitin', 0.4, 'Hard plating from a creature'],
  [/^(bone|horn|antler|tusk|ivory|fang|claw|talon|tooth|.*_bone|.*_fang|.*_tooth|cartilage|baleen)$/, 'material', 'creature_part', 'bone', 0.3, 'Skeletal material from a creature'],
  [/feather$|wing|silk|thread|strand/, 'material', 'fiber', 'organic', 0.05, 'Light material from a creature'],
  [/gland$|sac$|^troll_blood$|slime$|^adhesive_slime$|gel|ichor|ectoplasm|^bio_acid$/, 'material', 'creature_part', 'organic', 0.2, 'Organic extract from a creature'],
  [/crystal|shard|gem|core$|^battery$|^charged_/, 'material', 'exotic', 'crystal', 0.3, 'Energetic material from a creature'],
  [/essence|dust$|ash$|ember|spirit|soul|shadow|mana|ether|void|flame$|brimstone|infernal/, 'material', 'exotic', 'essence', 0.05, 'Exotic residue from a creature'],
  [/^(scrap_metal|rusty_weapon|crude_weapon|armor_plate|circuit_board|data_chip|servo_motor|drill_bit|lens|weapon_core|rare_alloy|nanite_cluster|synthetic_skin|silicon_shell|infernal_plate)$/, 'material', 'salvage', 'metal', 0.8, 'Salvage from a mechanical creature'],
  [/^(ore_sample|rare_ore|enchanted_stone|stone_heart|mineral_claw)$/, 'material', 'ore', 'stone', 1.0, 'Mineral matter from a creature'],
  [/^(coin_pouch|wisdom_token|bait|seed_of_life|legendary_shard|rebirth_ember)$/, 'material', 'exotic', 'organic', 0.1, 'Rare find from a creature'],
];
const DEFAULT = ['material', 'creature_part', 'organic', 0.2, 'Part harvested from a creature'];

function titleCase(id) {
  return id.split('_').map((w) => w.charAt(0).toUpperCase() + w.slice(1)).join(' ');
}

const creatures = fs.readFileSync(CREATURES, 'utf8');
const lootIds = new Set();
for (const line of creatures.split('\n')) {
  const t = line.trim();
  if (t === '' || t.startsWith('#') || t.startsWith('id,')) continue;
  const cols = t.split(',');
  const loot = cols[14] || '';
  for (const seg of loot.split('|')) {
    const id = seg.split(':')[0];
    if (id) lootIds.add(id);
  }
}

const itemsText = fs.readFileSync(ITEMS, 'utf8');
const existing = new Set();
for (const line of itemsText.split('\n')) {
  const t = line.trim();
  if (t === '' || t.startsWith('#') || t.startsWith('id,')) continue;
  existing.add(t.split(',')[0]);
}

const rows = [];
for (const raw of [...lootIds].sort()) {
  if (existing.has(raw) || existing.has(raw + '_0')) continue;
  let rule = DEFAULT;
  for (const [re, ...rest] of RULES) {
    if (re.test(raw)) { rule = rest; break; }
  }
  const [category, subcategory, material, weight, desc] = rule;
  const name = titleCase(raw);
  const volume = Math.max(0.05, +(weight * 1.5).toFixed(2));
  rows.push(
    `${raw}_0,${name},${category},${subcategory},${material},${weight},20,0,${desc},solid,${volume}`
  );
}

if (rows.length === 0) {
  console.log('all creature loot ids already have items');
} else {
  const out = itemsText.replace(/\n?$/, '\n') +
    '# Creature loot drops (generated by scripts/gen-loot-items.js, v0.760)\n' +
    rows.join('\n') + '\n';
  fs.writeFileSync(ITEMS, out);
  console.log(`appended ${rows.length} loot item rows to data/items.csv`);
}
